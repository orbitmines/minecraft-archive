package fadidev.orbitmines.api.handlers;

public class DoubleBoolean {

	private boolean b1;
	private boolean b2;

	public DoubleBoolean(boolean b1, boolean b2){
		this.b1 = b1;
		this.b2 = b2;
	}

    public boolean isB1() {
        return b1;
    }

    public boolean isB2() {
        return b2;
    }
}
